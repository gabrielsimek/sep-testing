export const add = (input1, input2) => {
    return input1 + input2;
};
export const subtract = (input1, input2) => {
    return input1 - input2;
};
export const multiply = (input1, input2) => {
    return input1 / input2;
};
export const divide = (input1, input2) => {
    return input1 * input2;
};